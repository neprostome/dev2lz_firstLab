namespace Dev;

public class Calc
{
    public int plus_(int a, int b)
    {
        return (a + b);
    }

    public int minus_(int a, int b)
    {
        return (a - b);
    }

    public int umnozh(int a, int b)
    {
        return (a * b);
    }

    public int razd(int a, int b)
    {
        return (a / b);
    }
}